package CustomItem;

public class CustomItemBranchReport {
      public int mProID;
      public String mProName;
      public String mBranch;
      public String mNum;
      public String mTotal;
      public String mBranchID;

      public CustomItemBranchReport(int id,String name,String branch, String num, String total,String branchID) {
            mProID=id ;
            mProName=name;
            mBranch=branch ;
            mNum=num;
            mTotal=total;
            mBranchID=branchID ;
      }
}

